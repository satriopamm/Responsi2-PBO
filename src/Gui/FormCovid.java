    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
       String valid = edid.getText();
        String valNama = ednama.getText();
        String valAlamat = edalamat.getText();
        String valGender = edgender.getSelectedItem().toString();
        String valStatus = edstatus.getSelectedItem().toString();
        
        Connection objCon = new ConnectDB().getConnection();
        
        try {
            String query_insert = "insert into pasien values (?, ?, ?, ?, ?)";
            PreparedStatement statement = objCon.prepareStatement(query_insert);

            statement.setString(1, valid);
            statement.setString(2, valNama);
            statement.setString(3, valAlamat);
            statement.setString(4, valGender);
            statement.setString(5, valStatus);
            
            statement.executeUpdate();  
            objCon.close();
            JOptionPane.showMessageDialog(null, "Berhasil Memasukkan data");
           
            statement.close();
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Gagal Memasukkan data");
            Logger.getLogger(FormCovid.class.getName()).log(Level.SEVERE, null, ex);
        }
        init();
        tampilData();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
         String valid = edid.getText();
        String valnama = ednama.getText();
        String valAlamat = edalamat.getText();
        String valGender = edgender.getSelectedItem().toString();
        String valstatus = edstatus.getSelectedItem().toString();
        
        try {
            Connection objCon = new ConnectDB().getConnection();
            String query_update = "update pasien set nama='" + valnama
                    +"', alamat='" + valAlamat
                    +"', gender ='" + valGender
                    +"', status='" + valstatus
                    +"' where no_pasien='" + valid + "'";
        
            Statement statement = objCon.createStatement();
            statement.executeUpdate(query_update);
            JOptionPane.showMessageDialog(null, "Berhasil Mengubah data");
            statement.close();
                   
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Data gagal diubah");
            Logger.getLogger(FormCovid.class.getName()).log(Level.SEVERE, null, ex);
        }
        init();
        tampilData();
    }//GEN-LAST:event_jButton3ActionPerformed

