    private void waktu(){
        new Thread(){
            int waktumulai;
            @Override
            public void run(){
                while (waktumulai==0){
                    Calendar calendar = new GregorianCalendar();
                    int jam = calendar.get(Calendar.HOUR);
                    int menit = calendar.get(Calendar.MINUTE);
                    int detik = calendar.get(Calendar.SECOND);
                    int AM_PM = calendar.get(Calendar.AM_PM);
                    String siang_malam = "";
                    if (AM_PM==1){
                        siang_malam="PM";
                    }else{
                        siang_malam="AM";
                    }
                    String time = jam+" : "+menit+" : "+detik;
                    String day = siang_malam;
                    jLabel7.setText(time);
                    jLabel8.setText(day);
                }
            }
        }.start();
    }
    
    private void tampilData(){
        // menampilkan kedalam model tabel
      
        
        Connection obj = new ConnectDB().getConnection();
        
        try {
            Statement s = obj.createStatement();
            ResultSet res = s.executeQuery("Select * from pasien");
            
            while (res.next()){
                Object [] ob = new Object[5];
                ob[0] = res.getString("No_pasien"); //nim adalah nama kolom pada tabel
                ob[1] = res.getString("Nama");
                ob[2] = res.getString("Alamat");
                ob[3] = res.getString("Gender");
                ob[4] = res.getString("Status");
                
                model.addRow(ob);
            }
            tbcovid.setModel(model);
            s.close();
        } catch (SQLException e) {
            Logger.getLogger(FormCovid.class.getName()).log(Level.SEVERE, null, e);
        }

        }

